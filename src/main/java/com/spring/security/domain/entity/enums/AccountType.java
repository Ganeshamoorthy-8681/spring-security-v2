package com.spring.security.domain.entity.enums;

/**
 * Enum representing the type of account.
 *
 * <p>This enum defines the following account types:
 *
 * <ul>
 *   <li>INDIVIDUAL - Represents an individual account.
 *   <li>ORGANIZATION - Represents an organization account.
 * </ul>
 */
public enum AccountType {

  /** Represents an individual account. */
  INDIVIDUAL,

  /** Represents an organization account. */
  ORGANIZATION,
}
